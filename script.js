// ============ THEME TOGGLE ============
function initializeTheme() {
    const savedTheme = localStorage.getItem('theme') || 'light';
    isDarkMode = savedTheme === 'dark';
    applyTheme(isDarkMode);
}

function toggleTheme() {
    isDarkMode = !isDarkMode;
    localStorage.setItem('theme', isDarkMode ? 'dark' : 'light');
    applyTheme(isDarkMode);
}

function applyTheme(dark) {
    const body = document.body;
    const themeToggle = document.getElementById('themeToggle');

    // styles.css يعتمد على body.dark-mode
    if (dark) {
        body.classList.add('dark-mode');
        if (themeToggle) themeToggle.innerHTML = '<i class="fas fa-sun"></i>';
    } else {
        body.classList.remove('dark-mode');
        if (themeToggle) themeToggle.innerHTML = '<i class="fas fa-moon"></i>';
    }
}


// ============ LANGUAGE TOGGLE ============
function initializeLanguage() {
    const savedLang = localStorage.getItem('language') || 'en';
    currentLang = savedLang;
    applyLanguage(currentLang);
}

function toggleLanguage() {
    currentLang = currentLang === 'en' ? 'ar' : 'en';
    localStorage.setItem('language', currentLang);
    applyLanguage(currentLang);
}

function applyLanguage(lang) {
    const html = document.documentElement;
    const body = document.body;
    const langToggle = document.getElementById('langToggle');

    html.lang = lang;
    html.dir = lang === 'ar' ? 'rtl' : 'ltr';

    if (lang === 'ar') {
        body.classList.add('rtl');
        if (langToggle) langToggle.textContent = 'EN';
    } else {
        body.classList.remove('rtl');
        if (langToggle) langToggle.textContent = 'ع';
    }

    updateTranslations(lang);
}


function updateTranslations(lang) {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');

        // keep HTML inside element (important when element is a <a> etc)
        // but translations are plain text so we use textContent
        if (translations[lang] && Object.prototype.hasOwnProperty.call(translations[lang], key)) {
            element.textContent = translations[lang][key];
            return;
        }

        if (translations.en && Object.prototype.hasOwnProperty.call(translations.en, key)) {
            element.textContent = translations.en[key];
        }
    });
}


